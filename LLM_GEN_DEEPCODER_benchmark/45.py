def triangle_area(a, h):
    """
    Calculate the area of a triangle given the length of a side and the corresponding height.

    Parameters:
    a (float or int): Length of the side of the triangle.
    h (float or int): Height corresponding to the side 'a'.

    Returns:
    float: Area of the triangle calculated as (a * h) / 2.

    Example:
    >>> triangle_area(5, 3)
    7.5
    """
    return (a * h) / 2